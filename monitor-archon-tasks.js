#!/usr/bin/env node

/**
 * 🤖 Archon Task Monitor
 * Monitors Archon for new tasks every 30 minutes and logs them
 * Run with: node monitor-archon-tasks.js
 */

const fs = require('fs');
const path = require('path');

class ArchonTaskMonitor {
    constructor() {
        this.projectId = '26c4c114-f142-4720-9417-ca471a50e84d';
        this.lastCheckTime = new Date();
        this.checkInterval = 30 * 60 * 1000; // 30 minutes
        this.logFile = path.join(__dirname, 'archon-tasks.log');
        this.processedTasks = new Set();

        // Load previously processed tasks
        this.loadProcessedTasks();
    }

    loadProcessedTasks() {
        try {
            if (fs.existsSync(this.logFile)) {
                const log = fs.readFileSync(this.logFile, 'utf8');
                const lines = log.split('\n');
                lines.forEach(line => {
                    const match = line.match(/Task ID: ([a-f0-9-]+)/);
                    if (match) {
                        this.processedTasks.add(match[1]);
                    }
                });
            }
        } catch (error) {
            console.error('Error loading processed tasks:', error);
        }
    }

    async checkForNewTasks() {
        console.log(`\n🔍 [${new Date().toISOString()}] Checking Archon for new tasks...`);

        // This is a placeholder for actual Archon API call
        // In production, this would use the actual MCP API
        const mockTasks = [
            {
                id: 'task-' + Date.now(),
                title: 'Example Task',
                description: 'This is a mock task',
                status: 'todo',
                assignee: 'AI IDE Agent',
                priority: 'medium'
            }
        ];

        const newTasks = mockTasks.filter(task => !this.processedTasks.has(task.id));

        if (newTasks.length > 0) {
            console.log(`\n📋 Found ${newTasks.length} new task(s):`);

            newTasks.forEach(task => {
                this.logTask(task);
                this.createImplementationReminder(task);
                this.processedTasks.add(task.id);
            });
        } else {
            console.log('✅ No new tasks found');
        }

        this.lastCheckTime = new Date();
        console.log(`⏰ Next check scheduled for: ${new Date(Date.now() + this.checkInterval).toLocaleString()}`);
    }

    logTask(task) {
        const logEntry = `
================================================================================
📅 Timestamp: ${new Date().toISOString()}
📋 Task ID: ${task.id}
📌 Title: ${task.title}
📝 Description: ${task.description}
👤 Assignee: ${task.assignee}
🎯 Status: ${task.status}
⚡ Priority: ${task.priority || 'normal'}
================================================================================
`;

        console.log(logEntry);
        fs.appendFileSync(this.logFile, logEntry);
    }

    createImplementationReminder(task) {
        const reminderFile = path.join(__dirname, `TODO_IMPLEMENT_${task.id}.md`);
        const reminderContent = `# 🚨 IMPLEMENTATION REQUIRED

## Task: ${task.title}

**ID**: ${task.id}
**Assigned**: ${task.assignee}
**Created**: ${new Date().toISOString()}

## Description
${task.description}

## Implementation Checklist
- [ ] Review task requirements
- [ ] Create implementation plan
- [ ] Write code
- [ ] Test implementation
- [ ] Update Archon task status
- [ ] Commit changes to git

## Notes
This file was auto-generated by the Archon Task Monitor.
Delete this file once the task is completed.
`;

        fs.writeFileSync(reminderFile, reminderContent);
        console.log(`📄 Created reminder file: ${reminderFile}`);
    }

    start() {
        console.log('🚀 Archon Task Monitor Started');
        console.log(`📊 Project ID: ${this.projectId}`);
        console.log(`⏱️ Check interval: ${this.checkInterval / 1000 / 60} minutes`);
        console.log(`📁 Log file: ${this.logFile}`);

        // Initial check
        this.checkForNewTasks();

        // Schedule periodic checks
        setInterval(() => {
            this.checkForNewTasks();
        }, this.checkInterval);
    }

    // Create a summary of pending tasks
    async generateTaskSummary() {
        const summaryFile = path.join(__dirname, 'ARCHON_TASK_SUMMARY.md');
        const summary = `# 📊 Archon Task Summary

**Generated**: ${new Date().toISOString()}
**Project ID**: ${this.projectId}

## 📋 Task Statistics
- Total processed tasks: ${this.processedTasks.size}
- Last check: ${this.lastCheckTime.toISOString()}
- Next check: ${new Date(this.lastCheckTime.getTime() + this.checkInterval).toISOString()}

## 🔄 Monitoring Status
- Monitor running: ✅
- Check interval: ${this.checkInterval / 1000 / 60} minutes
- Log file: ${this.logFile}

## 📝 Processed Task IDs
${Array.from(this.processedTasks)
        .map(id => `- ${id}`)
        .join('\n')}

## 💡 Instructions
1. Run monitor: \`node monitor-archon-tasks.js\`
2. Check logs: \`cat archon-tasks.log\`
3. View reminders: \`ls TODO_IMPLEMENT_*.md\`

---
*Auto-generated by Archon Task Monitor*
`;

        fs.writeFileSync(summaryFile, summary);
        console.log(`📊 Task summary saved to: ${summaryFile}`);
    }
}

// Create and start monitor
const monitor = new ArchonTaskMonitor();

// Handle graceful shutdown
process.on('SIGINT', () => {
    console.log('\n\n👋 Shutting down Archon Task Monitor...');
    monitor.generateTaskSummary();
    process.exit(0);
});

process.on('SIGTERM', () => {
    console.log('\n\n👋 Shutting down Archon Task Monitor...');
    monitor.generateTaskSummary();
    process.exit(0);
});

// Start monitoring
monitor.start();

// Keep process alive
process.stdin.resume();

console.log('\n💡 Press Ctrl+C to stop monitoring\n');
